    private void loadCountries() {
        comboModelCountries = new DefaultComboBoxModel<>();
        
//        List<OptionKeyValue> citiesList = ddlManager.getAllCities();
//        List<OptionKeyValue> countriesList = ddlManager.getAllCountries();
        //ddlManager.getAllCitiesByCountry(3).forEach(c -> comboModelCities.addElement(c));
        ddlManager.getAllCountries().forEach(c -> comboModelCountries.addElement(c));

        //cbGrad.setModel(comboModelCities);
        cbDrzava.setModel(comboModelCountries);
        if (comboModelCountries.getSize() != 0) {
            cbDrzava.setSelectedIndex(0);
        }
    }

    private void loadCities(int countryId) {
        comboModelCities = new DefaultComboBoxModel<>();
        ddlManager = new DdlDataManager();
        ddlManager.getAllCitiesByCountry(countryId).forEach(c -> comboModelCities.addElement(c));
        cbGrad.setModel(comboModelCities);
    }

